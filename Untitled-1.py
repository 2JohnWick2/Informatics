import matplotlib.pyplot as plt
import pandas as pd

# Записываем данные сопротивления (R) и мощности (P) из предоставленной таблицы.
# Эти данные были прочитаны и введены вручную из загруженного изображения.
data = {
    'R_ohm': [4.813, 4.838, 4.754, 4.786, 4.891, 4.923, 4.887, 4.956, 4.979, 5.008, 5.036, 5.066, 5.091],
    'P_watt': [0.433, 0.774, 1.189, 1.723, 2.397, 3.150, 3.958, 4.956, 6.025, 7.212, 8.512, 9.929, 11.454]
}

# Создаем DataFrame с помощью pandas
df = pd.DataFrame(data)

# Строим график зависимости R от P
plt.figure(figsize=(10, 5))
plt.plot(df['P_watt'], df['R_ohm'], marker='o', linestyle='-', color='b')

# Добавляем заголовки и метки осей
plt.title('Зависимость сопротивления нити от мощности')
plt.xlabel('Мощность, Вт')
plt.ylabel('Сопротивление, Ом')

plt.legend()

# Отображаем график
plt.grid(True)
plt.show()